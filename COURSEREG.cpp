#include <iostream>



using namespace std;

int main() {
	   int t;
	   cin>>t;
	   while(t--)
	   {
	       int n,m,k,y;
	       cin>>n>>m>>k;
	       y=m-k;
	       if(y>=n)
	       {
	           cout<<"yes"<<endl;
	       }
	       else
	       {
	           cout<<"no"<<endl;
	       }
	       
	       
	   }
	return 0;
}
